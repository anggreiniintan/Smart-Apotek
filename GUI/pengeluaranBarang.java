    private void tampil(){
        
        DefaultTableModel modell= new DefaultTableModel();           
            modell.addColumn("No");
            modell.addColumn("ID");
            modell.addColumn("Harga");
            modell.addColumn("Tanggal keluar");
        try{
            String sql = "SELECT * FROM pembukuan";
            st=con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            int no =0;
            while(rs.next()){
                no++;
                modell.addRow(new Object[]{
                   no, rs.getString("id"), rs.getString("total_harga"),
                    rs.getString("tanggal_keluar")
                });
            }
            Tabelbarangkeluar.setModel(modell);
        }catch(Exception e){
            System.out.println(e.getLocalizedMessage());
        }
    }
    
     
