     private void tampilTabel() {
        dataObat.clear();
        dataObat = obat.tampil();
        for (int x = 0; x < dataObat.size(); x++) {
            Object[] data = new Object[3];
            data[0] = dataObat.get(x).getid();
            data[1] = dataObat.get(x).getNamaobat();
            data[2] = dataObat.get(x).getHarga();
            tab.addRow(data);
        }
    }
   
