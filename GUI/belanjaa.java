    private void pencarianKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pencarianKeyPressed
        DefaultTableModel tab= new DefaultTableModel(); // mendeklarasikan class yang digunakan untuk memberi header dan data
            tab.addColumn("ID"); // menambahkan kolom
            tab.addColumn("Nama Obat");
            tab.addColumn("Harga");
            tabeldat.setModel(tab);
        //Untuk mengatur lebar kolom
        tabeldat.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);                
        tabeldat.getColumnModel().getColumn(0).setPreferredWidth(200);
        tabeldat.getColumnModel().getColumn(1).setPreferredWidth(300);        
        tabeldat.getColumnModel().getColumn(2).setPreferredWidth(200);        
       

            
        try{
            String sql = "SELECT * FROM dataobat WHERE id LIKE '%"+pencarian.getText()+
                    "%' or nama_obat LIKE '%"+pencarian.getText()+
                    "%' or harga LIKE '%"+pencarian.getText()+
                    "%'";
            st=con.createStatement();
            rs=st.executeQuery(sql);
            
            while(rs.next()){ 
                tab.addRow(new Object[]{
                rs.getString("id"), rs.getString("nama_obat"),rs.getString("harga")
                });
            }
            tabeldat.setModel(tab);
        }catch(Exception e){
            System.out.println(e.getLocalizedMessage());
        }
    }//GEN-LAST:event_pencarianKeyPressed

